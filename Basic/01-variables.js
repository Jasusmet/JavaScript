console.log(helloWorld2)

helloWorld2 = "¡Hola de nuevo, JavaScript!"
console.log(helloWorld2)

// const "es ideal para declarar constantes que no cambian."

const helloWorld3 = "¡Hola, JavaScript!"
console.log(helloWorld3)

